<h1>Other Reports</h1>
<p>The Commission of Inquiry into Money Laundering in British Columbia was announced on May 15, 2019 by Premier John Horgan. The decision to proceed with a commission of inquiry followed four independent reviews that found extraordinary levels of money laundering in B.C.’s real estate market, casinos and other sectors of the economy.</p>
<p>The reports are:</p>
<ul>
    <li>Dirty Money: An Independent Review of Money Laundering in Lower Mainland Casinos Conducted for the Attorney General of British Columbia <a href="https://news.gov.bc.ca/files/Gaming_Final_Report.pdf" target="_blank">https://news.gov.bc.ca/files/Gaming_Final_Report.pdf</a> Peter M. German, Q.C., March 31, 2018;</li>
    <li>Dirty Money – Part 2: Turning the Tide – An Independent Review of Money Laundering in B.C. Real Estate, Luxury Vehicle Sales & Horse Racing <a href="https://news.gov.bc.ca/files/Dirty_Money_Report_Part_2.pdf" target="_blank">https://news.gov.bc.ca/files/Dirty_Money_Report_Part_2.pdf</a> Peter M. German, Q.C., March 31, 2019;</li>
    <li>Combatting Money Laundering in BC Real Estate <a href="https://news.gov.bc.ca/files/Combatting_Money_Laundering_Report.pdf" target="_blank">https://news.gov.bc.ca/files/Combatting_Money_Laundering_Report.pdf</a> Maureen Maloney, Tsur Somerville and Brigitte Unger, March 31, 2019; and</li>
    <li>Real Estate Regulatory Structure Review <a href="https://news.gov.bc.ca/files/Real_Estate_Regulatory_Structure_Review_Report_2018.pdf" target="_blank">https://news.gov.bc.ca/files/Real_Estate_Regulatory_Structure_Review_Report_2018.pdf</a> Dan Perrin, 2018.</li>
</ul>

