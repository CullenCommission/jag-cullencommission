<h2><strong>Providing Information to the Inquiry</strong></h2>
<p>The Commission of Inquiry into Money Laundering in British Columbia is a public inquiry, serving the people of British Columbia. We are independent from the government and other agencies.</p>
<p>We want to engage with the people of British Columbia throughout the Inquiry.</p>
<p>If you have information about money laundering activities, we would like to hear from you.</p>
<p>Money laundering is, by its nature, secretive and hidden, but this public inquiry represents an opportunity to shed light on this important topic. If you have information that you would like to provide to Senior Commission Counsel, please email: <a href="mailto:contact@cullencommission.ca">contact@cullencommission.ca</a> or leave a message at <a href="tel:+18444683158">1-844-468-3158</a>.</p>
<p>The personal details included with your information or document is collected for fulfilling the Commission’s Terms of Reference and may be used by the Commission for the purposes of its Inquiry.</p>
<p>Information and submissions provided to the Commission will not be posted online and your personal information will not be published or shared without Commission Counsel or staff speaking with you first.</p>
<p>We may not be able to respond to all communications, but we will do our best. Thank you for your understanding, and for supporting the important work of this Commission.</p>
