<h1>About</h1>
<p>The Commission of Inquiry into Money Laundering in British Columbia was announced on May 15, 2019 by Premier John Horgan. The decision to proceed with a commission of inquiry followed four independent reviews that found extraordinary levels of money laundering in B.C.’s real estate market, casinos and other sectors of the economy.</p>
<p>The reports are:</p>
<ul>
    <li><em>Dirty Money: An Independent Review of Money Laundering in Lower Mainland Casinos Conducted for the Attorney General of British Columbia</em> <a href="https://news.gov.bc.ca/files/Gaming_Final_Report.pdf" target="_blank">https://news.gov.bc.ca/files/Gaming_Final_Report.pdf</a>, Peter M. German, Q.C., March 31, 2018;</li>
    <li><em>Dirty Money – Part 2: Turning the Tide – An Independent Review of Money Laundering in B.C. Real Estate, Luxury Vehicle Sales &amp; Horse Racing</em> <a href="https://news.gov.bc.ca/files/Dirty_Money_Report_Part_2.pdf" target="_blank">https://news.gov.bc.ca/files/Dirty_Money_Report_Part_2.pdf</a>, Peter M. German, Q.C., March 31, 2019;</li>
    <li><em>Combatting Money Laundering in BC Real Estate</em> <a href="https://news.gov.bc.ca/files/Combatting_Money_Laundering_Report.pdf" target="_blank">https://news.gov.bc.ca/files/Combatting_Money_Laundering_Report.pdf</a>, Maureen Maloney, Tsur Somerville and Brigitte Unger, March 31, 2019; and</li>
    <li><em>Real Estate Regulatory Structure Review</em> <a href="https://news.gov.bc.ca/files/Real_Estate_Regulatory_Structure_Review_Report_2018.pdf" target="_blank">https://news.gov.bc.ca/files/Real_Estate_Regulatory_Structure_Review_Report_2018.pdf</a>, Dan Perrin, 2018.</li>
</ul>
<p>B.C. Supreme Court Justice Austin F. Cullen has been appointed to head the Inquiry, which looks at the full scope of money laundering in British Columbia, including real estate, gaming, financial institutions and the corporate and professional sectors. Commissioner Cullen is also examining regulatory authorities and barriers to effective law enforcement of money laundering activities. Commissioner Cullen has the ability to compel witnesses and order the production of documents and records.</p>
<p>The Commission of Inquiry into Money Laundering in British Columbia is required to deliver an interim report 18 months from the date of being announced and a final report by May 2021.</p>
<p>The Commission Team can be <a href="/inquiry-team/">found here</a>.</p>
<h2>Terms of Reference</h2>
<p>The scope of the Commission’s task is set out in the terms of reference which are reproduced below:</p>
<p>Commission of Inquiry into Money Laundering in British Columbia</p>
<p>Terms of reference</p>
<ol>
    <li>The terms of reference of the commission are to conduct hearings and make findings of fact respecting money laundering in British Columbia, including the following:
        <ol class="LowerAlphaList">
            <li>the extent, growth, evolution and methods of money laundering in the following sectors:
                <ol class="LowerRomanList">
                    <li>gaming and horse racing;</li>
                    <li>real estate;</li>
                    <li>financial institution and money service, including unregulated entities and persons who provide banking-like services;</li>
                    <li>corporate, in relation to the use of shell companies, trusts, securities and financial instruments for the purposes of money laundering;</li>
                    <li>luxury goods;</li>
                    <li>professional service, including legal and accounting;</li>
                </ol>
            </li>
            <li>the acts or omissions of regulatory authorities or individuals with powers, duties or functions in respect of the sectors referred to in paragraph (a), or any other relevant sector, to determine whether those acts or omissions have contributed to money laundering in British Columbia and whether those acts or omissions have amounted to corruption;</li>
            <li>the scope and effectiveness of the powers, duties and functions exercised or carried out by the regulatory authorities or individuals referred to in paragraph (b);</li>
            <li>the barriers to effective law enforcement respecting money laundering in British Columbia.</li>
        </ol>
    </li>
    <li>Further terms of reference of the commission are as follows:
        <ol class="LowerAlphaList">
            <li>to make recommendations the commission considers necessary and advisable, including recommendations respecting the following:
                <ol>
                    <li>the regulation of the sectors referred to in subsection (1) (a) or any other relevant sector;</li>
                    <li>the acts or omissions referred to in subsection (1) (b);</li>
                    <li>the powers, duties and functions referred to in subsection (1) (c);</li>
                    <li>the barriers referred to in subsection (1) (d);</li>
                </ol>
            </li>
            <li>to review and take into consideration the following reports:
                <ol>
                    <li><em>Dirty Money: An Independent Review of Money Laundering in Lower Mainland Casinos</em> conducted for the Attorney General of British Columbia, Peter M. German, Q.C., March 31, 2018;</li>
                    <li><em>Vancouver at Risk—Turning the Tide—An Independent Review of Money Laundering in B.C. Real Estate, Luxury Vehicle Sales &amp; Horse Racing</em>, Peter M. German, Q.C., March 31, 2019;</li>
                    <li><em>Real Estate Regulatory Structure Review</em> (2018), Dan Perrin;</li>
                    <li><em>Combatting Money Laundering in BC Real Estate</em>, Maureen Maloney, Tsur Somerville and Brigitte Unger, March 31, 2019;</li>
                </ol>
            </li>
            <li>to submit to the Attorney General and the Minister of Finance
                <ol>
                    <li>an interim report on or before the date that is 18 months after the date this order is made, and</li>
                    <li>a final report on or before the date that is 2 years after the date this order is made.</li>
                </ol>
            </li>
        </ol>
    </li>
    <li>If the commissioner has reasonable grounds to believe that any information obtained during the inquiry may be useful in the investigation or prosecution of an offence under the Criminal Code, the commissioner must forward that information to the appropriate authorities.</li>
    <li>The commission is to carry out the inquiry in such a way as to ensure the inquiry does not jeopardize any ongoing criminal investigation or proceeding.</li>
    <li>The commission may not inquire into any matter respecting the exercise of prosecutorial discretion.</li>
</ol>
