<h1>Terms of Reference</h1>
<p><strong>Definitions</strong></p>
<p><strong>1</strong></p>
<p>In this order:</p>
<p><strong>&ldquo;Act&rdquo;</strong> means the <em>Public Inquiry Act</em>;<br /> <strong>&ldquo;commission&rdquo;</strong> means the commission established under section 2 of this order;<br /> <strong>&ldquo;money laundering&rdquo;</strong> means the process used to disguise the source of money or assets derived from illegal activity.</p>
<p><strong>Establishment of commission</strong></p>
<p><strong>2</strong></p>
<ol class="NumberedList">
    <li>A study and hearing commission called the Commission of Inquiry into Money Laundering in British Columbia is established under section 2 of the Act.</li>
    <li>The Honourable Justice Austin F. Cullen is the sole commissioner of the Commission.</li>
</ol>
<p><strong>Purposes of commission</strong></p>
<p><strong>3</strong></p>
<p>The purposes of the commission are as follows:</p>
<ol class="LowerAlphaList">
    <li>to inquire into and report on money laundering in British Columbia;</li>
    <li>to make recommendations referred to in section 4 (2) (a).</li>
</ol>
<p><strong>Terms of reference</strong></p>
<p><strong>4</strong></p>
<ol class="NumberedList">
    <li>The terms of reference of the commission are to conduct hearings and make findings of fact respecting money laundering in British Columbia, including the following:
        <ol class="LowerAlphaList">
            <li>the extent, growth, evolution and methods of money laundering in the following sectors:
                <ol class="LowerRomanList">
                    <li>gaming and horse racing;</li>
                    <li>real estate;</li>
                    <li>financial institution and money service, including unregulated entities and persons who provide banking-like services;</li>
                    <li>corporate, in relation to the use of shell companies, trusts, securities and financial instruments for the purposes of money laundering;</li>
                    <li>luxury goods;</li>
                    <li>professional service, including legal and accounting;</li>
                </ol>
            </li>
            <li>the acts or omissions of regulatory authorities or individuals with powers, duties or functions in respect of the sectors referred to in paragraph (a), or any other relevant sector, to determine whether those acts or omissions have contributed to money laundering in British Columbia and whether those acts or omissions have amounted to corruption;</li>
            <li>the scope and effectiveness of the powers, duties and functions exercised or carried out by the regulatory authorities or individuals referred to in paragraph (b);</li>
            <li>the barriers to effective law enforcement respecting money laundering in British Columbia.</li>
        </ol>
    </li>
    <li>Further terms of reference of the commission are as follows:
        <ol class="LowerAlphaList">
            <li>to make recommendations the commission considers necessary and advisable, including recommendations respecting the following:
                <ol class="LowerRomanList">
                    <li>the regulation of the sectors referred to in subsection (1) (a) or any other relevant sector;</li>
                    <li>the acts or omissions referred to in subsection (1) (b);</li>
                    <li>the powers, duties and functions referred to in subsection (1) (c);</li>
                    <li>the barriers referred to in subsection (1) (d);</li>
                </ol>
            </li>
            <li>to review and take into consideration the following reports:
                <ol>
                    <li><em>Dirty Money: An Independent Review of Money Laundering in Lower Mainland Casinos</em>conducted for the Attorney General of British Columbia, Peter M. German, Q.C., March 31, 2018;</li>
                    <li><em>Vancouver at Risk&mdash;Turning the Tide&mdash;An Independent Review of Money Laundering in B.C. Real Estate, Luxury Vehicle Sales &amp; Horse Racing</em>, Peter M. German, Q.C., March 31, 2019;</li>
                    <li><em>Real Estate Regulatory Structure Review</em> (2018), Dan Perrin;</li>
                    <li><em>Combatting Money Laundering in BC Real Estate</em>, Maureen Maloney, Tsur Somerville and Brigitte Unger, March 31, 2019;</li>
                </ol>
            </li>
            <li>to submit to the Attorney General and the Minister of Finance
                <ol>
                    <li>an interim report on or before the date that is 18 months after the date this order is made, and</li>
                    <li>a final report on or before the date that is 2 years after the date this order is made.</li>
                </ol>
            </li>
        </ol>
    </li>
    <li>If the commissioner has reasonable grounds to believe that any information obtained during the inquiry may be useful in the investigation or prosecution of an offence under the <em>Criminal Code</em>, the commissioner must forward that information to the appropriate authorities.</li>
    <li>The commission is to carry out the inquiry in such a way as to ensure the inquiry does not jeopardize any ongoing criminal investigation or proceeding.</li>
    <li>The commission may not inquire into any matter respecting the exercise of prosecutorial discretion.</li>
</ol>
<p><strong>Expenses</strong></p>
<p><strong>5</strong></p>
<p>Subject to the directives of Treasury Board, the commissioner is entitled to be reimbursed for reasonable travelling and living expenses at the rates specified for Group III employees set out in the government&rsquo;s Core Policy and Procedures Manual.</p>

